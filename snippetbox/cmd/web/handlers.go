package main

import (
	"fmt"
	"github.com/vision8tech/snippetbox/pkg/models"
	"net/http"
	"strconv"
)

//
//
//
func (app *application) home(w http.ResponseWriter, r *http.Request) {

	if r.URL.Path != "/" {
		app.notFound(w)
		return
	}

	snippets, err := app.snippetsStore.Latest()
	if err != nil {
		app.serverError(w, err)
		return
	}

	app.render(w, r, "home.page.gohtml", &templateData{Snippets: snippets})

}

//
//
//
func (app *application) showSnippet(w http.ResponseWriter, r *http.Request) {

	id, err := strconv.Atoi(r.URL.Query().Get("id"))
	if err != nil || id < 1 {
		app.notFound(w)
		return
	}
	// Find a snippet with the provided id.
	snippet, err := app.snippetsStore.Get(id)
	if err == models.ErrNoRecord {
		app.notFound(w)
		return
	} else if err != nil {
		app.serverError(w, err)
		return
	}
	app.render(w, r, "show.page.gohtml", &templateData{Snippet: snippet})

}

